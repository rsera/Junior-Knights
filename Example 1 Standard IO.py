# Standard IO Example Sheet

# This is a simple print statement
print("Hello world!")

# This is a variable initialization
num = 5

# This is a print statement with 2 arguments: a string and a varible
print("The number is", num)

# This is a user input statement
# When it executes, it waits for the user to type on their keyboard
# and hit enter when they are done
# Input statements return values. The value should be stored in a variable.
name = input("What is your name?")

# Let's print what's in the variable name to see what the user typed
print(name)

# This is an input statement when you want a data type other than a string for your result
mins = int(input("How many minutes did you study programming this week?"))

# Let's print the number of minutes the user entered
print(mins)


